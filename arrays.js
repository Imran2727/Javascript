//methods

//method--push()
//adds new element to the end of the string/array and returns new length of string

let mob=["oppo","vivo","oneplus"]
console.log(mob)
let qq=mob.length
console.log(qq)
let ww=mob.push("nokia")
console.log(ww)
console.log(mob)

//method--unshift()
//adds new element to the start of string/array and returns new length of string
let ee=mob.unshift("sony")
console.log(ee)
console.log(mob)


//method--pop()
//removes the element from the end of string/array and returns same element
let rr=mob.pop()
console.log(rr)
console.log(mob)

//method--shift()
//removes the element from the start of the string/array and returns same element
let tt=mob.shift()
console.log(tt)
console.log(mob)
